func requireBodyMatchUpdateUser(t *testing.T, body *bytes.Buffer, user db.User) {
	data, err := io.ReadAll(body)
	require.NoError(t, err)

	var got struct {
		User db.User `json:"user"`
	}
	err = json.Unmarshal(data, &got)
	require.NoError(t, err)

	require.Equal(t, user.Username, got.User.Username)
	require.Equal(t, user.FullName, got.User.FullName)
	require.Equal(t, user.Email, got.User.Email)
	require.Empty(t, got.User.HashedPassword)
}

func randomUser(t *testing.T) (user db.User, password string) {
	password = util.RandomString(10)
	hashedPassword, err := util.HashPassword(password)
	require.NoError(t, err)

	user = db.User{
		Username:       util.RandomName(6),
		HashedPassword: hashedPassword,
		FullName:       util.RandomName(6),
		Email:          util.RandomEmail(),
	}
	return
}

func requireBodyMatchUser(t *testing.T, body *bytes.Buffer, user db.User) {
	data, err := io.ReadAll(body)
	require.NoError(t, err)

	var gotUser db.User
	err = json.Unmarshal(data, &gotUser)

	require.NoError(t, err)
	require.Equal(t, user.Username, gotUser.Username)
	require.Equal(t, user.FullName, gotUser.FullName)
	require.Equal(t, user.Email, gotUser.Email)
	require.Empty(t, gotUser.HashedPassword) // 确保响应中不包含哈希密码
}
